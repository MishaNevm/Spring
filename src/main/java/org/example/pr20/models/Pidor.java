package org.example.pr20.models;

public class Pidor {
    private String name;
    private String prostupok;
    private int prostupukRating;
    private int id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProstupok() {
        return prostupok;
    }

    public void setProstupok(String prostupok) {
        this.prostupok = prostupok;
    }

    public int getProstupukRating() {
        return prostupukRating;
    }

    public void setProstupukRating(int prostupukRating) {
        this.prostupukRating = prostupukRating;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
